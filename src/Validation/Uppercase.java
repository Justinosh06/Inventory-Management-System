package Validation;

// The Uppercase class provides utility methods for string manipulation related to uppercase conversion.
public class Uppercase {
    // Converts a given string to uppercase.
    // @param str The input string to be converted.
    // @return The uppercase version of the input string.
    public static String setUppercase(String str){
        return str.toUpperCase(); // Use the built-in toUpperCase() method of the String class.
    }
}