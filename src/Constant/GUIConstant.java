package Constant;

import javax.swing.*;
import java.awt.Color;

// The GUIConstant class defines constant Color objects used throughout the graphical user interface (GUI) of the application.
// This provides a centralized location for defining and accessing consistent color schemes.
public class GUIConstant {
    // A constant Color object representing white.
    public static Color white1 = Color.white;
    // A constant Color object representing a light grey, decoded from the hexadecimal color code "#ececec".
    public static Color white2 = Color.decode("#ececec");
    // A constant Color object representing black, decoded from the hexadecimal color code "#111111".
    public static Color black = Color.decode("#111111");
    // A constant Color object representing grey, decoded from the hexadecimal color code "#A6A6A6".
    public static Color grey = Color.decode("#A6A6A6");
    // A constant Color object representing dark grey, decoded from the hexadecimal color code "#444444".
    public static Color darkGrey = Color.decode("#444444");
    // A constant Color object representing red.
    public static Color red = Color.red;
}